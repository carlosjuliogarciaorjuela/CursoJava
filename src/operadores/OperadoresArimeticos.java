package operadores;

public class OperadoresArimeticos {
    public static void main(String[] args) {


        // operador suma
        int x = 100 + 100;
        System.out.println(x);

        int suma1 = 100 + 50;
        System.out.println(suma1);

        int suma2 = suma1 + 50;
        System.out.println(suma2);

        int suma3 = suma1 + suma2;
        System.out.println(suma3);

        // Operador Subtraction
        int a = 5;
        int b = 3;
        System.out.println(a - b);

        // Multiplication
        int z = 5;
        int y = 2;
        System.out.println(z * y);

        // Division
        int n = 12;
        int m = 3;
        System.out.println(n / m);

        // Modulos
        int n1 = 5;
        int m2 = 2;
        System.out.println(n1 % m2);

        // Increment
        int num1 = 5;
        ++num1;
        System.out.println(num1);

        // Decrement
        int num2 = 5;
        --num2;
        System.out.println(num2);


















    }
}
